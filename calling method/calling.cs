// Create a namespace for the application
namespace MathApp
{
    // Create a class called MathOperations
    public class MathOperations
    {
        // Method 1:
        // This method takes an integer parameter, adds 10 to it, and returns the result
        public int AddTen(int number)
        {
            return number + 10;
        }

        // Method 2:
        // This method takes an integer parameter, multiplies it by 5, and returns the result
        public int MultiplyByFive(int number)
        {
            return number * 5;
        }

        // Method 3:
        // This method takes an integer parameter, subtracts 3 from it, and returns the result
        public int SubtractThree(int number)
        {
            return number - 3;
        }
    }
}